struct Matrix4x4
{
	double m[4][4];
};

struct Vector3
{
	float x;
	float y;
	float z;
};

const char kWindowTitle[] = "LE2B_12_コバヤシ_ダイスケ";
Matrix4x4 MakeRotateXMatrix(float theta) {
	Matrix4x4 result;
	result.m[0][0] = 1;
	result.m[0][1] = 0;
	result.m[0][2] = 0;
	result.m[0][3] = 0;
	result.m[1][0] = 0;
	result.m[1][1] = cosf(theta);
	result.m[1][2] = sinf(theta);
	result.m[1][3] = 0;
	result.m[2][0] = 0;
	result.m[2][1] = -sinf(theta);
	result.m[2][2] = cosf(theta);
	result.m[2][3] = 0;
	result.m[3][0] = 0;
	result.m[3][1] = 0;
	result.m[3][2] = 0;
	result.m[3][3] = 1;
	return result;
}
Matrix4x4 MakeRotateYMatrix(float theta) {
	Matrix4x4 result;
	result.m[0][0] = cosf(theta);
	result.m[0][1] = 0;
	result.m[0][2] = -sinf(theta);
	result.m[0][3] = 0;
	result.m[1][0] = 0;
	result.m[1][1] = 1;
	result.m[1][2] = 0;
	result.m[1][3] = 0;
	result.m[2][0] = sinf(theta);
	result.m[2][1] = 0;
	result.m[2][2] = cosf(theta);
	result.m[2][3] = 0;
	result.m[3][0] = 0;
	result.m[3][1] = 0;
	result.m[3][2] = 0;
	result.m[3][3] = 1;
	return result;
}
Matrix4x4 MakeRotateZMatrix(float theta) {
	Matrix4x4 result;
	result.m[0][0] = cosf(theta);
	result.m[0][1] = sinf(theta);
	result.m[0][2] = 0;
	result.m[0][3] = 0;
	result.m[1][0] = -sinf(theta);
	result.m[1][1] = cosf(theta);
	result.m[1][2] = 0;
	result.m[1][3] = 0;
	result.m[2][0] = 0;
	result.m[2][1] = 0;
	result.m[2][2] = 1;
	result.m[2][3] = 0;
	result.m[3][0] = 0;
	result.m[3][1] = 0;
	result.m[3][2] = 0;
	result.m[3][3] = 1;
	return result;
}
