Matrix4x4 Multiply(const Matrix4x4& m1, const Matrix4x4& m2) {
	Matrix4x4 result;
	double term = 0.0f;
	for (int i = 0; i < 4; i++) {
		for (int j = 0; j < 4; j++) {
			term = 0;
			for (int k = 0; k < 4; k++) {
				term = term + m1.m[i][k] * m2.m[k][j];
				result.m[i][j] = term;
			}
		}
	}
	return result;
}